def stripTags(pageContents):
    startLoc = pageContents.find("<hr/><h2>")
 
    pageContents = pageContents[startLoc:]

    inside = 0
    text = ''
    for char in pageContents:
        if char == '<':
            inside = 1
        elif (inside == 1 and char == '>'):
            inside = 0
        elif inside == 1:
            continue
        else:
            text += char
    return text




# Given a text string, remove all non-alphanumeric
# characters (using Unicode definition of alphanumeric).

# Rockson: I've hacked the shit out of this to make areally messy hack to treat latex umlauts...
# Now it converts html codes into text, then removes \\" string from text.
# Converts everything to lower case
# Splits into a list

def stripNonAlphaNum(text):
   
    from HTMLParser import HTMLParser
    h = HTMLParser()    
    text2=h.unescape(text).encode('ascii','ignore')
    
    REMOVE_LIST = ['\\"','\\it']
    REPLACE_LIST = ['-']
    for remove in REMOVE_LIST:
        text2=text2.replace(remove,'')
    for replace in REPLACE_LIST:
        text2=text2.replace(replace,' ')
    out=text2.lower()
    return out.split()

    

# Given a list of words, return a dictionary of
# word-frequency pairs.

def wordListToFreqDict(wordlist):
    wordfreq = [wordlist.count(p) for p in wordlist]
    return dict(zip(wordlist,wordfreq))


# Sort a dictionary of word-frequency pairs in
# order of descending frequency.

def sortFreqDict(freqdict):
    aux = [(freqdict[key], key) for key in freqdict]
    aux.sort()
    aux.reverse()
    return aux


# Given a list of words, remove any that are
# in a list of stop words.

def removeStopwords(wordlist, stopwords):
    return [w for w in wordlist if w not in stopwords]

